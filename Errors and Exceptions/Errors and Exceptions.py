
# --------ERRORS AND EXCEPTIONS---------

# Even if a statement or expression is syntactically correct,
# it may cause an error when an attempt is made to execute it.
# Errors detected during execution are called exceptions

# for example
print(1/0) # ZeroDivisionError: division by zero

# The last line of the error message indicates what happened.
# It provides detail based on the type of exception and what caused it.
# The preceding part of the error message shows the context where the exception happened, in the form of a stack traceback.